private void settableload()
{
    String stat = "";
    try
    {
        Class.forName(driver);
        Connection kon = DriverManager.getConnection(database,user,pass);
        Statement stt = kon.createStatement();
        String SQL ="SELECT\n" +
"    `tabel_detail_transaksi_cicil`.`nama_barang`\n" +
"    ,`tabel_detail_transaksi_cicil`.`alamat_pembeli`\n" +
"    , `tabel_detail_transaksi_cicil`.`banyak_barang`\n" +
"    , `tabel_detail_transaksi_cicil`.`harga_satuan`\n" +
"    , `tabel_transaksi_cicil`.`kode_pembeli_cicil`\n" +
"    , `tabel_transaksi_cicil`.`nama_pembeli`\n" +
"    , `tabel_transaksi_cicil`.`keterangan`\n" +
"    , `tabel_transaksi_cicil`.`tanggal_pembeli`\n" +
"    , `tabel_detail_transaksi_cicil`.`harga_total`\n" +
"    , `tabel_detail_transaksi_cicil`.`uang_muka`\n" +
"    , `tabel_detail_transaksi_cicil`.`sisa`\n" +
"    , `tabel_detail_transaksi_cicil`.`tenggang_waktu`\n" +
                
"FROM\n" +
"    `10114255_mebel`.`tabel_detail_transaksi_cicil`\n" +
"    INNER JOIN `10114255_mebel`.`tabel_transaksi_cicil` \n" +
"        ON (`tabel_detail_transaksi_cicil`.`kode_pembeli_cicil` = `tabel_transaksi_cicil`.`kode_pembeli_cicil`) GROUP BY kode_pembeli_cicil";
        ResultSet res = stt.executeQuery(SQL);
        while (res.next()) 
        {
           data[0] = res.getString(5);//kode
           data[1] = res.getString(8);//tanggal beli
           data[2] = res.getString(12);//tenggang waktu
           data[3] = res.getString(6);//nama
           data[4] = res.getString(2);//alamat
           data[5] = res.getString(1);//nama barang
           data[6] = res.getString(4);//harga satuan
           data[7] = res.getString(3);//jumalah beli
           data[8] = res.getString(10);//uang muka
           data[9] = res.getString(7);//nama pembeli
           tableModel.addRow(data);
        }
        res.close();
        stt.close();
        kon.close();
    }
    catch(Exception ex)
    {
        System.err.println(ex.getMessage());
        JOptionPane.showMessageDialog(null, ex.getMessage(),"Error",JOptionPane.INFORMATION_MESSAGE);
        System.exit(0);
    }
}
