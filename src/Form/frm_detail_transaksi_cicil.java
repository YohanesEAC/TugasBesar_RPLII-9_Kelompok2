"    `10114255_mebel`.`tabel_detail_transaksi_cicil`\n" +
"    INNER JOIN `10114255_mebel`.`tabel_transaksi_cicil` \n" +
"        ON (`tabel_detail_transaksi_cicil`.`kode_pembeli_cicil` = `tabel_transaksi_cicil`.`kode_pembeli_cicil`) where `tabel_transaksi_cicil`.`kode_pembeli_cicil`" + " like '%" + cari.getText() + "%'";
